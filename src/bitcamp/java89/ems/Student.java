package bitcamp.java89.ems;

public class Student {
  // 인스턴스 변수
  String userId;
  String password;
  String name;
  String email;
  String tel;
  boolean working;
  int birthYear;
  String school;

  public Student() {}

  public Student(String userId, String password, String name, String tel) {
    this.userId = userId;
    this.password = password;
    this.name = name;
    this.tel = tel;
  }





}
